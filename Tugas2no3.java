public class Tugas2no3 {

    public static void main(String[] args) {
        char a = '4';
        char b = '6';

        int asci1 = (int) a;
        int asci2 = (int) b;

        int total = asci1 + asci2;

        System.out.println("Kode ASCII dari '" + a + "' adalah: " + asci1);
        System.out.println("Kode ASCII dari '" + b + "' adalah: " + asci2);
        System.out.println("Hasil penjumlahan kode ASCII: " + total);
    }
}
